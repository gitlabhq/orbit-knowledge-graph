//! Schema-migration ledger and drift detection.
//!
//! The ledger (`config/schema-migrations.yaml`) records, per `SCHEMA_VERSION`
//! bump, how much of the graph that version invalidates, so the clone-based
//! migration path (a later MR) can decide what to clone versus rebuild. Drift is
//! detected against a committed fingerprint snapshot
//! (`config/schema-migrations.fingerprint.yaml`); this module owns the ledger
//! model, the fingerprints, and the scope math, shared by the build-time check
//! and the `xtask` tooling.
//!
//! Two caveats the mechanics can't enforce:
//!
//! - SDLC plan lowering (`crates/indexer/src/modules/sdlc/plan/`) is runtime code
//!   a build script can't reach, so a change there that alters extract/transform
//!   SQL without touching YAML is not caught — the human widens the entry, same
//!   trust model as the `SCHEMA_VERSION` bump discipline.
//! - An entry carries one scope: a version touching both SDLC entities and
//!   code-graph nodes derives `"*"` rather than a compound scope.

use std::collections::{BTreeMap, BTreeSet};

use serde::{Deserialize, Serialize};
use sha2::{Digest, Sha256};

use crate::Ontology;

/// Ledger path relative to `CONFIG_DIR`.
pub const LEDGER_FILE: &str = "schema-migrations.yaml";
/// Fingerprint-snapshot path relative to `CONFIG_DIR`.
pub const FINGERPRINT_FILE: &str = "schema-migrations.fingerprint.yaml";

const FINGERPRINT_HEADER: &str = "\
# GENERATED by `mise schema:bump` — do not edit.
#
# Fingerprint snapshot of the current ontology + generated DDL. Drift against
# this file (recomputed at build time and in CI) forces a SCHEMA_VERSION bump
# and a schema-migrations.yaml entry. Describes current state only; history
# lives in git.
";

const LEDGER_HEADER: &str = "\
# yaml-language-server: $schema=schemas/schema-migrations.schema.json
#
# One entry per SCHEMA_VERSION bump, appended by `mise schema:bump`.
# Each entry declares how much of the graph a given version invalidates:
#   scope: \"*\"    full rebuild (fail-safe; the default for anything unmapped)
#   scope: sdlc   SDLC-sourced tables; optional `entities:` narrows to a subset
#   scope: code   the code-graph tables (Definition, File, Directory, Branch,
#                 ImportedSymbol) and their edge table
#
# Humans may WIDEN an entry (scope or entities), never narrow it below the
# drift detected from the fingerprint snapshot — CI enforces this. A version
# with no entry is treated as \"*\", which also makes pruning old entries safe.
";

/// Lowercase hex SHA-256 of `input`. Shared with the compiler crate so its DDL
/// fingerprints hash identically without a second sha2 dependency.
#[must_use]
pub fn sha256_hex(input: &str) -> String {
    use std::fmt::Write as _;

    let mut hasher = Sha256::new();
    hasher.update(input.as_bytes());
    let digest = hasher.finalize();
    let mut out = String::with_capacity(digest.len() * 2);
    for byte in digest {
        let _ = write!(out, "{byte:02x}");
    }
    out
}

/// Hash of the canonicalized YAML parse: every mapping is sorted by the
/// serialized form of its keys, so comment- or ordering-only edits produce the
/// same hash. Non-YAML content is hashed raw.
#[must_use]
pub fn canonical_yaml_hash(content: &str) -> String {
    match serde_yaml::from_str::<serde_yaml::Value>(content) {
        Ok(mut value) => {
            canonicalize(&mut value);
            match serde_yaml::to_string(&value) {
                Ok(rendered) => sha256_hex(&rendered),
                Err(_) => sha256_hex(content),
            }
        }
        Err(_) => sha256_hex(content),
    }
}

fn canonicalize(value: &mut serde_yaml::Value) {
    match value {
        serde_yaml::Value::Mapping(map) => {
            let mut entries: Vec<(serde_yaml::Value, serde_yaml::Value)> =
                std::mem::take(map).into_iter().collect();
            for (_, v) in &mut entries {
                canonicalize(v);
            }
            entries.sort_by_key(|(key, _)| serialized_key(key));
            for (k, v) in entries {
                map.insert(k, v);
            }
        }
        serde_yaml::Value::Sequence(seq) => {
            for v in seq {
                canonicalize(v);
            }
        }
        _ => {}
    }
}

fn serialized_key(key: &serde_yaml::Value) -> String {
    serde_yaml::to_string(key).unwrap_or_default()
}

/// Per-file hash of every embedded ontology source, keyed by path relative to
/// `config/ontology/`. In debug builds rust-embed reads the live folder, so
/// tooling always fingerprints the working tree.
#[must_use]
pub fn source_fingerprints() -> BTreeMap<String, String> {
    crate::loading::embedded_files()
        .into_iter()
        .map(|(path, content)| {
            let hash = if path.ends_with(".yaml") || path.ends_with(".yml") {
                canonical_yaml_hash(&content)
            } else {
                sha256_hex(&content)
            };
            (path, hash)
        })
        .collect()
}

/// Every embedded ontology source file as path → contents. Feeds
/// [`derive_scope`], which re-parses changed node and derived files.
#[must_use]
pub fn embedded_sources() -> BTreeMap<String, String> {
    crate::loading::embedded_files().into_iter().collect()
}

/// The committed drift snapshot: source hashes plus generated-DDL hashes. The
/// compiler crate fills `ddl`; [`source_fingerprints`] fills `sources`.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct Fingerprints {
    pub sources: BTreeMap<String, String>,
    pub ddl: BTreeMap<String, String>,
}

impl Fingerprints {
    pub fn parse(content: &str) -> Result<Self, String> {
        serde_yaml::from_str(content).map_err(|e| format!("parsing fingerprint snapshot: {e}"))
    }

    /// Serialized YAML with the do-not-edit header.
    #[must_use]
    pub fn render(&self) -> String {
        let body = serde_yaml::to_string(self).expect("fingerprints serialize");
        format!("{FINGERPRINT_HEADER}{body}")
    }

    /// `(changed source paths, changed ddl tables)` between `self` (current)
    /// and `base`: a key present on only one side, or present on both with a
    /// different hash.
    #[must_use]
    pub fn diff(&self, base: &Fingerprints) -> (BTreeSet<String>, BTreeSet<String>) {
        (
            changed_keys(&self.sources, &base.sources),
            changed_keys(&self.ddl, &base.ddl),
        )
    }
}

fn changed_keys(
    current: &BTreeMap<String, String>,
    base: &BTreeMap<String, String>,
) -> BTreeSet<String> {
    let mut changed = BTreeSet::new();
    for (key, hash) in current {
        if base.get(key) != Some(hash) {
            changed.insert(key.clone());
        }
    }
    for key in base.keys() {
        if !current.contains_key(key) {
            changed.insert(key.clone());
        }
    }
    changed
}

/// How much of the graph an entry (or a derived drift) invalidates.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum Scope {
    /// Full rebuild.
    #[serde(rename = "*")]
    All,
    /// SDLC-sourced tables. Pairs with an optional `entities:` subset.
    #[serde(rename = "sdlc")]
    Sdlc,
    /// Code-graph tables.
    #[serde(rename = "code")]
    Code,
}

/// One `SCHEMA_VERSION` bump.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct MigrationEntry {
    pub version: u32,
    pub scope: Scope,
    #[serde(default, skip_serializing_if = "BTreeSet::is_empty")]
    pub entities: BTreeSet<String>,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub note: Option<String>,
}

#[derive(Debug, Clone, PartialEq, Eq, Default, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct MigrationLedger {
    #[serde(default)]
    pub migrations: Vec<MigrationEntry>,
}

const LEDGER_EMBEDDED: &str = include_str!(concat!(env!("CONFIG_DIR"), "/schema-migrations.yaml"));

impl MigrationLedger {
    pub fn load_embedded() -> Result<Self, String> {
        Self::parse(LEDGER_EMBEDDED)
    }

    pub fn parse(content: &str) -> Result<Self, String> {
        serde_yaml::from_str(content).map_err(|e| format!("parsing migration ledger: {e}"))
    }

    /// Serialized YAML with the usage header.
    #[must_use]
    pub fn render(&self) -> String {
        let body = serde_yaml::to_string(self).expect("ledger serializes");
        format!("{LEDGER_HEADER}{body}")
    }

    #[must_use]
    pub fn last(&self) -> Option<&MigrationEntry> {
        self.migrations.last()
    }

    /// Structural validation: versions strictly increasing, the last version
    /// equals `schema_version`, `entities:` appears only with `scope: sdlc`,
    /// and every named entity resolves in the SDLC name set.
    pub fn validate(&self, ontology: &Ontology, schema_version: u32) -> Result<(), String> {
        if self.migrations.is_empty() {
            return Err("migration ledger has no entries".into());
        }

        let mut prev: Option<u32> = None;
        for entry in &self.migrations {
            if let Some(prev) = prev
                && entry.version <= prev
            {
                return Err(format!(
                    "ledger versions must strictly increase: {prev} is followed by {}",
                    entry.version
                ));
            }
            prev = Some(entry.version);

            if !entry.entities.is_empty() && entry.scope != Scope::Sdlc {
                return Err(format!(
                    "version {}: `entities:` is only valid with `scope: sdlc`",
                    entry.version
                ));
            }
        }

        let last = self.last().expect("non-empty checked above");
        if last.version != schema_version {
            return Err(format!(
                "last ledger version {} must equal SCHEMA_VERSION {schema_version}",
                last.version
            ));
        }

        let names = sdlc_entity_names(ontology);
        for entry in &self.migrations {
            for entity in &entry.entities {
                if !names.contains(entity) {
                    return Err(format!(
                        "version {}: entity '{entity}' is not an SDLC entity or relationship kind",
                        entry.version
                    ));
                }
            }
        }

        Ok(())
    }

    /// What migrating from `active_version` to `embedded_version` must
    /// invalidate: the union of every entry's scope over the half-open range
    /// `(active_version, embedded_version]`. A version in that range with no
    /// ledger entry is fail-safe — the whole graph is invalidated.
    #[must_use]
    pub fn invalidation_between(
        &self,
        active_version: u32,
        embedded_version: u32,
    ) -> InvalidationScope {
        if embedded_version <= active_version {
            return InvalidationScope::none();
        }

        let entries_by_version: BTreeMap<u32, &MigrationEntry> =
            self.migrations.iter().map(|e| (e.version, e)).collect();

        let mut scope = InvalidationScope::none();
        for version in (active_version + 1)..=embedded_version {
            match entries_by_version.get(&version) {
                Some(entry) => scope = scope.union(&entry.invalidation_scope()),
                None => return InvalidationScope::full(),
            }
        }
        scope
    }
}

impl MigrationEntry {
    /// This entry's own scope and entities.
    #[must_use]
    pub fn declaration(&self) -> ScopeDeclaration {
        ScopeDeclaration {
            scope: self.scope,
            entities: self.entities.clone(),
        }
    }

    #[must_use]
    fn invalidation_scope(&self) -> InvalidationScope {
        match self.scope {
            Scope::All => InvalidationScope::full(),
            Scope::Code => InvalidationScope {
                sdlc: SdlcScope::None,
                code: true,
            },
            Scope::Sdlc if self.entities.is_empty() => InvalidationScope {
                sdlc: SdlcScope::All,
                code: false,
            },
            Scope::Sdlc => InvalidationScope {
                sdlc: SdlcScope::Entities(self.entities.clone()),
                code: false,
            },
        }
    }
}

/// The resolved set of tables a migration must invalidate, composed from one or
/// more ledger entries.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct InvalidationScope {
    pub sdlc: SdlcScope,
    pub code: bool,
}

/// SDLC side of an [`InvalidationScope`].
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum SdlcScope {
    None,
    All,
    Entities(BTreeSet<String>),
}

impl InvalidationScope {
    #[must_use]
    pub fn none() -> Self {
        Self {
            sdlc: SdlcScope::None,
            code: false,
        }
    }

    #[must_use]
    pub fn full() -> Self {
        Self {
            sdlc: SdlcScope::All,
            code: true,
        }
    }

    #[must_use]
    pub fn union(&self, other: &Self) -> Self {
        Self {
            sdlc: self.sdlc.union(&other.sdlc),
            code: self.code || other.code,
        }
    }
}

impl SdlcScope {
    #[must_use]
    fn union(&self, other: &Self) -> Self {
        match (self, other) {
            (SdlcScope::All, _) | (_, SdlcScope::All) => SdlcScope::All,
            (SdlcScope::None, s) | (s, SdlcScope::None) => s.clone(),
            (SdlcScope::Entities(a), SdlcScope::Entities(b)) => {
                SdlcScope::Entities(a.union(b).cloned().collect())
            }
        }
    }
}

/// Names accepted in an entry's `entities:` list: etl-bearing nodes, derived
/// entities, and relationship kinds. Code-graph nodes (etl-less) are excluded —
/// they re-index per project, not per entity.
#[must_use]
pub fn sdlc_entity_names(ontology: &Ontology) -> BTreeSet<String> {
    let mut names = BTreeSet::new();
    for node in ontology.nodes() {
        if node.etl.is_some() {
            names.insert(node.name.clone());
        }
    }
    for derived in ontology.derived_entities() {
        names.insert(derived.name.clone());
    }
    for kind in ontology.edge_names() {
        names.insert(kind.to_string());
    }
    names
}

/// A scope paired with the SDLC entities that narrow it — what a ledger entry
/// declares, and the minimal declaration [`derive_scope`] computes from drift.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ScopeDeclaration {
    pub scope: Scope,
    /// Empty means the whole scope; only `sdlc` declarations carry entities.
    pub entities: BTreeSet<String>,
}

impl ScopeDeclaration {
    #[must_use]
    fn all() -> Self {
        Self {
            scope: Scope::All,
            entities: BTreeSet::new(),
        }
    }

    #[must_use]
    fn code() -> Self {
        Self {
            scope: Scope::Code,
            entities: BTreeSet::new(),
        }
    }

    #[must_use]
    fn sdlc(entities: BTreeSet<String>) -> Self {
        Self {
            scope: Scope::Sdlc,
            entities,
        }
    }

    /// Whether this declaration covers `required` (the minimal drift). `"*"`
    /// covers everything; `code` covers only `code`; `sdlc` with no entities
    /// covers any SDLC drift; `sdlc` with entities covers only subsets of them.
    #[must_use]
    pub fn covers(&self, required: &Self) -> bool {
        match (self.scope, required.scope) {
            (Scope::All, _) => true,
            (_, Scope::All) => false,
            (Scope::Code, Scope::Code) => true,
            (Scope::Code, Scope::Sdlc) | (Scope::Sdlc, Scope::Code) => false,
            (Scope::Sdlc, Scope::Sdlc) => {
                if self.entities.is_empty() {
                    true
                } else {
                    !required.entities.is_empty() && required.entities.is_subset(&self.entities)
                }
            }
        }
    }

    /// The widest of the two declarations (a symmetric union): used when human
    /// flags widen a derived scope, or when an amend folds new drift into an
    /// existing entry. Mixing `code` and `sdlc` widens to `"*"`.
    #[must_use]
    pub fn widen(&self, other: &Self) -> Self {
        match (self.scope, other.scope) {
            (Scope::All, _) | (_, Scope::All) => Self::all(),
            (Scope::Code, Scope::Code) => Self::code(),
            (Scope::Code, Scope::Sdlc) | (Scope::Sdlc, Scope::Code) => Self::all(),
            (Scope::Sdlc, Scope::Sdlc) => {
                if self.entities.is_empty() || other.entities.is_empty() {
                    Self::sdlc(BTreeSet::new())
                } else {
                    Self::sdlc(self.entities.union(&other.entities).cloned().collect())
                }
            }
        }
    }
}

impl std::fmt::Display for ScopeDeclaration {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self.scope {
            Scope::All => write!(f, "scope \"*\""),
            Scope::Code => write!(f, "scope code"),
            Scope::Sdlc if self.entities.is_empty() => write!(f, "scope sdlc"),
            Scope::Sdlc => {
                let list = self.entities.iter().cloned().collect::<Vec<_>>().join(", ");
                write!(f, "scope sdlc, entities [{list}]")
            }
        }
    }
}

/// The minimal [`ScopeDeclaration`] a fingerprint diff justifies. Returns
/// `None` when nothing changed. Fail-safe: anything the rules cannot map
/// confidently widens to [`Scope::All`].
///
/// `source_contents` is the working tree's files by path (for re-parsing a
/// changed node/derived file); `changed_sources` and `changed_tables` name the
/// changed source paths and generated tables from a [`Fingerprints::diff`].
#[must_use]
pub fn derive_scope(
    ontology: &Ontology,
    source_contents: &BTreeMap<String, String>,
    changed_sources: &BTreeSet<String>,
    changed_tables: &BTreeSet<String>,
) -> Option<ScopeDeclaration> {
    if changed_sources.is_empty() && changed_tables.is_empty() {
        return None;
    }

    let mut sdlc_entities: BTreeSet<String> = BTreeSet::new();
    let mut code_changed = false;

    for path in changed_sources {
        if path == "schema.yaml" || path == "reference.yaml" {
            return Some(ScopeDeclaration::all());
        }
        if path.starts_with("nodes/") {
            match source_contents
                .get(path)
                .map(String::as_str)
                .and_then(parse_node_scope)
            {
                Some(NodeScope::Sdlc(entity)) => {
                    sdlc_entities.insert(entity);
                }
                Some(NodeScope::Code) => code_changed = true,
                None => return Some(ScopeDeclaration::all()),
            }
        } else if path.starts_with("edges/") {
            match edge_kind_for_path(ontology, path) {
                Some(relationship_kind) => {
                    sdlc_entities.insert(relationship_kind);
                }
                None => return Some(ScopeDeclaration::all()),
            }
        } else if path.starts_with("derived/") {
            match source_contents
                .get(path)
                .map(String::as_str)
                .and_then(parse_derived_name)
            {
                Some(entity) => {
                    sdlc_entities.insert(entity);
                }
                None => return Some(ScopeDeclaration::all()),
            }
        } else {
            return Some(ScopeDeclaration::all());
        }
    }

    for table in changed_tables {
        if !table_owned_by_scope(ontology, table, &sdlc_entities, code_changed) {
            return Some(ScopeDeclaration::all());
        }
    }

    if !sdlc_entities.is_empty() && code_changed {
        return Some(ScopeDeclaration::all());
    }
    if code_changed {
        return Some(ScopeDeclaration::code());
    }
    Some(ScopeDeclaration::sdlc(sdlc_entities))
}

enum NodeScope {
    Sdlc(String),
    Code,
}

#[derive(Deserialize)]
struct NodeScopeYaml {
    node_type: String,
    #[serde(default)]
    etl: Option<serde_yaml::Value>,
}

fn parse_node_scope(content: &str) -> Option<NodeScope> {
    let parsed: NodeScopeYaml = serde_yaml::from_str(content).ok()?;
    if parsed.etl.is_some() {
        Some(NodeScope::Sdlc(parsed.node_type))
    } else {
        Some(NodeScope::Code)
    }
}

#[derive(Deserialize)]
struct DerivedNameYaml {
    name: String,
}

fn parse_derived_name(content: &str) -> Option<String> {
    let parsed: DerivedNameYaml = serde_yaml::from_str(content).ok()?;
    Some(parsed.name)
}

/// `edges/<stem>.yaml` → the relationship kind (`<STEM>` uppercased) when it
/// resolves in the ontology.
fn edge_kind_for_path(ontology: &Ontology, path: &str) -> Option<String> {
    let stem = path
        .strip_prefix("edges/")?
        .strip_suffix(".yaml")?
        .to_uppercase();
    ontology.has_edge(&stem).then_some(stem)
}

/// Whether `table`'s DDL change is already explained by the scope collected so
/// far: a collected SDLC node's own table, a shared edge table a collected edge
/// routes to, or a code-graph table when `code_changed`. Anything else (e.g. an
/// auxiliary table like `checkpoint`) is unexplained and forces `"*"`.
fn table_owned_by_scope(
    ontology: &Ontology,
    table: &str,
    sdlc_entities: &BTreeSet<String>,
    code_changed: bool,
) -> bool {
    for entity in sdlc_entities {
        if let Some(node) = ontology.get_node(entity)
            && node.destination_table == table
        {
            return true;
        }
        if ontology.has_edge(entity) && ontology.edge_table_for_relationship(entity) == table {
            return true;
        }
    }
    code_changed && is_code_table(ontology, table)
}

fn is_code_table(ontology: &Ontology, table: &str) -> bool {
    ontology
        .nodes()
        .any(|n| n.etl.is_none() && n.destination_table == table)
        || table == "gl_code_edge"
}

