//! Shared utility helpers used across pipeline phases.
//!
//! These operations are needed by both `cngsetup` and `gkg`, so they live
//! here to avoid duplication.

use std::fs;

use anyhow::{Context, Result};
use xshell::Shell;

use super::config::Config;
use super::constants as c;
use super::kubectl;

// -- Tilt secrets -------------------------------------------------------------

/// Write the `.secrets` dotenv file that Tilt expects.
///
/// Reads the JWT secret from the toolbox pod and writes it along with the
/// PG password into `e2e/tilt/.secrets`.  Returns the path written.
pub fn write_tilt_secrets(
    sh: &Shell,
    cfg: &Config,
    toolbox_pod: &str,
    pg_pass: &str,
) -> Result<String> {
    let jwt_path = &cfg.pod_paths.jwt_secret_path;
    let jwt_secret = kubectl::toolbox_exec(sh, cfg, toolbox_pod, &["cat", jwt_path])
        .context("failed to read JWT secret from toolbox pod")?;

    fs::create_dir_all(&cfg.tilt_dir)?;
    let secrets_file = cfg.tilt_dir.join(c::SECRETS_FILE);
    let contents = format!(
        "# Auto-generated by cargo xtask e2e setup\n\
         POSTGRES_PASSWORD={pg_pass}\n\
         CLICKHOUSE_PASSWORD=\n\
         GKG_JWT_SECRET={jwt_secret}\n"
    );
    fs::write(&secrets_file, &contents)?;

    Ok(secrets_file.display().to_string())
}

// -- Test script copying ------------------------------------------------------

/// Copy all `.rb` test scripts from `e2e/tests/` into the toolbox pod.
pub fn copy_test_scripts(sh: &Shell, cfg: &Config, toolbox_pod: &str) -> Result<usize> {
    let local_dir = cfg.gkg_root.join(c::E2E_TESTS_DIR);
    kubectl::cp_files(
        sh,
        cfg,
        toolbox_pod,
        &cfg.namespaces.gitlab,
        &local_dir,
        &cfg.pod_paths.e2e_pod_dir,
        "rb",
    )
}
