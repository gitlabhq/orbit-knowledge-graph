use std::collections::HashSet;
use std::fs;
use std::path::PathBuf;

fn main() {
    println!("cargo:rerun-if-changed=build.rs");

    let config_dir = PathBuf::from(std::env::var("CONFIG_DIR").unwrap_or_else(|_| {
        panic!(
            "CONFIG_DIR must be set via .cargo/config.toml [env] when building the indexer crate"
        )
    }));

    let actions_path = config_dir
        .join("vendored")
        .join("system_note_metadata.actions");
    println!("cargo:rerun-if-changed={}", actions_path.display());

    let content = fs::read_to_string(&actions_path)
        .unwrap_or_else(|e| panic!("read {}: {e}", actions_path.display()));

    let actions: Vec<&str> = content
        .lines()
        .map(str::trim)
        .filter(|l| !l.is_empty() && !l.starts_with('#'))
        .collect();

    if actions.is_empty() {
        panic!(
            "{}: no actions found (file may be empty or malformed)",
            actions_path.display()
        );
    }

    let mut seen = HashSet::new();
    for action in &actions {
        if !seen.insert(*action) {
            panic!("{}: duplicate action {:?}", actions_path.display(), action);
        }
    }

    let mut out = String::from(
        "// @generated by build.rs from config/vendored/system_note_metadata.actions\n",
    );
    out.push_str("#[cfg(test)]\n");
    out.push_str("pub const ICON_TYPES: &[&str] = &[\n");
    for action in &actions {
        out.push_str(&format!("    {action:?},\n"));
    }
    out.push_str("];\n");

    let out_dir = PathBuf::from(std::env::var("OUT_DIR").expect("OUT_DIR set by cargo"));
    fs::write(out_dir.join("icon_types_generated.rs"), &out)
        .expect("write icon_types_generated.rs");
}
